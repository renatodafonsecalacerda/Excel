            	<p>
				If so, your managers and other readers must work <b>hard</b><i> </i>to 
				transform those numbers into useful insight. But your Excel 
				reports don't have to be that way. They could be <b>easy</b> to read 
				and understand. <br />
				<br />
				The figures at the right show dashboard reports created entirely with Excel. 
				Professional-quality reports like these work somewhat like the 
				dashboard of your car. At a glance, your readers can easily review a massive amount of data, quickly discover patterns of 
				performance in the data, and easily remember what they've seen. </p>
			<p>
				Excel users in more than half the countries in the world use 
				Excel templates like these to create reports for their clients, 
				co-workers, managers, or investors. You can do the same with 
				your own reports.<br />
				<br />
				Of course, if there's something you want to change about these 
				reports, you can do so easily. This isn't complex software. No 
				macros are needed. <b>It's only Excel.<br />
&nbsp;</b></p>
			<h3>
				High-Quality Reports</h3>
			<p>
				These reports look like they came from the pages of a business 
				magazine, or from the business graphics department of a 
				high-priced consulting firm. Unless you tell your managers, they 
				probably won't guess that you created the reports in Excel.</p>
			<p>
				As 
				Excel users, our purpose is to <b>communicate business 
				information</b> clearly and quickly. Our purpose is <strong>not
				</strong>to 
				impress our audience with pretty pictures. <br />
				<br />
				Therefore, these reports avoid chart junk...the distracting eye 
				candy that many commercial dashboard companies add to their 
				expensive dashboard systems. This is why my reports don't 
				include gauges, 3D images, or other visual toys that act as a 
				speed bump to undertanding. Instead, these reports are designed to
				<b>clearly</b> show information that your audience needs.<br />
&nbsp;</p>
			<h3>
				Fast-Updating Reports</h3>
			<p>
				<i>Well-designed </i>Excel dashboard reports offer two 
				important benefits; one is for people who read the reports, the other is for 
				Excel users who prepare them.</p>
			<p>
				Readers get simple, <b>
				professional-quality reports</b>. This feature is important because the 
				reports will make it easy for your readers to get more 
				information, more quickly, than any other reporting format can 
				provide.</p>
			<p>
				And
				Excel users get dynamic updating, which means quick and easy 
				reporting. Here's how...</p>
			<p><b>You can update these reports quickly because they typically contain no data!</b> Instead, 
			they contain only a few settings, along with formulas linked to data worksheets in separate workbooks. </p>
			<p>When it's time to update your reports, you...</p>
			<ol>
				<li>Add new data to a column in each data worksheet (your Actual 
				and Target worksheets, for example). <br />
&nbsp;</li>
				<li>Update a date value in one cell of your report. <br />
&nbsp;</li>
				<li>Recalculate your workbook. (If Automatic Calculation is 
				set, this happens automatically. Otherwise, you press F9.)</li>
		</ol>
		<p><b>Any number of DB2 and QnE2 reports</b> can link to the same data worksheets. 
		This means that you update your data worksheets once and then use them 
		in any number of dynamically updated reports. </p>
			<p>This reporting strategy 
		can... </p>
			<ul>
				<li>save you many hours of work each month, because all reports 
				benefit from the one data update.<br />
&nbsp;</li>
				<li>improve the accuracy of your reports, because all reports 
				get their data from the same few data worksheets.</li>
			</ul>
			<p>I designed these reports to <b>avoid Worksheet Hell</b>. The 
			reports all link to two simple data worksheets. One contains actual 
			performance data, the other contains target data. (If you don't need 
			target data, just enter zeros for your targets.)</p>
			<h3><br />
			Inexpensive Reports</h3>
			<p>
				Because these templates offer such great value, I originally planned 
				to charge hundreds of dollars for them. But I decided to lower the 
				price so that as many people as possible can generate 
				their own Excel dashboard reports.<br />
				<br />
				But charging hundreds of dollars would have been a bargain, 
				because... </p>
			<ul>
				<li>Commercial dashboard software typically costs tens of 
				thousands of dollars and takes many days to set up. But my 
				reports provide more information per report than the expensive 
				systems, and my reports are easier to read.<br />
&nbsp;</li>
				<li>Custom Excel dashboard reports of this quality could cost thousands of 
				dollars for just a few reports. But IncSight DB, Version 2, includes twenty 
				report designs for a very low price.<br />
&nbsp;</li>
				<li>None of the expensive solutions would allow you to generate 
				your first reports in a few hours, and modify them easily. But 
				you can do that with my reports.<br />
&nbsp;</li>
				<li>None of the expensive solutions would teach you Excel 
				techniques that you could use with your other Excel reports. But 
				that's exactly what IncSight DB2 and its 25 pages of 
				documentation allow.</li>
			</ul>
			<p>This package offers significant benefits that the expensive 
			systems lack. However, instead of thousands of dollars, or even 
			hundreds of dollars, I've set the price at about $3.00 per report. <br />
			<br />
			Get started today and distribute your first Excel dashboard report by 
		tomorrow!</p>
	
				
				
