				<p><b><i>It's impossible </i></b>for Excel formulas to change chart settings. That's what we're told.</p>
				<p>But the following examples show that these add-ins give 
				Excel formulas the ability to do the &quot;impossible&quot; and 
				add significant new power to your Excel charts.</p>
				<p>I've created a new name for those &quot;impossible&quot; worksheet functions-that-cause-actions: 
				<b>worksheet factions</b>. You use factions in worksheet formulas just like you use 
				ordinary functions like SUM or LOOKUP. But my factions actually 
				do change your charts.</p>
		<p>The following examples—which are included with the add-ins—will give you some ideas for using this new 
		power. The examples rely on these add-in products:</p>
				<ul>
					<li><b>Kyd Series</b> gets and sets the settings for 
					chart lines, markers, and data labels. It also includes two bonus add-ins:
					<b>Kyd&nbsp;Colors&nbsp;Lite</b> and<b> 
		Kyd&nbsp;Labels Lite</b>. <br />
					</li>
					<li><b>Kyd Axes</b> gets and sets the settings for chart axes. </li>
				</ul>
				<br/>
				<h3>Example 1: Show 3D Performance in Two Dimensions</h3>
				<p>These charts show <i>three</i> dimensions of performance in 
				two-dimension charts. They show Year, Sales Rank, and Sales Amount.</p>
				<p>The position of each marker shows the year and sales rank. And the 
				size of the marker adds the third dimension by indicating the amount 
				of sales. From an Excel perspective, <i>each chart marker 
				works like a simple column 
				or bubble chart</i>. </p>
				<p>I set up these charts so the amount of sales determines the <i>height</i> of the square 
				markers and the <i>area</i> of the circle 
				markers. I explain the formulas in the documentation.</p>
				<p>To see how the extra dimension helps to explain the data, 
				notice that as the rank of Hats (blue) fell 
				from first to third, its sales actually declined. But the sales of 
				Coats (red) increased significantly, even though its rank 
				ended where it began. </p>
				<p>Also notice that as you read down the column of markers for 
				any year, each column begins with a large marker and ends with a 
				small marker...as you would expect as you move from a 
				high rank to a low one.</p>
				<p>A typical Excel chart of ranked data couldn't 
				reveal that amount of information. But factions give them the 
				power to do so.</p>
				<p>This example relies primarily on the <strong>KydSetMarkerSize</strong> 
				worksheet faction. It gives 
				spreadsheet formulas the power to calculate what size the chart 
				markers must be to properly represent the amount of sales in 
				each instance, and then it sets the required marker size in the 
				chart.</p>
				<p>To illustrate, this formula sets the size of the second round blue marker in the second chart for this 
				example:</p>
				<p><strong>=KydSetMarkerSize($E$6,1,2,,19.6856796462313)</strong></p>
				<p>Here, cell E6 identifies the chart by specifying any cell 
				that the chart covers. The number 1 specifies the chart's first 
				series. The number 2 specifies the second point in that series. 
				And the final number specifies the size of the marker, which was 
				calculated by a separate formula. (The actual formula uses cell 
				references throughout, of course.)<br />&nbsp;
				</p>
				<h3>
				Example 2: Diagnostic Charts 
				for Stock Analysis</h3>
				<p>Normal charts reveal patterns of performance. Diagnostic charts 
				help to uncover the <i>reasons</i> for that performance. </p>
				<p>One way 
				to turn normal charts into diagnostic charts is to mark points 
				in a chart to signal conditions that might have affected performance...in 
				this case, stock performance. </p>
				<p>In fact, using Excel formulas to find patterns and to mark plots of stock 
				performance could add a whole new dimension to stock 
				analysis. </p>
				<p>However, our ability to highlight stock plots is limited with 
				Excel's standard stock charts, as shown in the first and third 
				charts for this example.</p>
				<p>On the other hand, the second and fourth charts look somewhat like the 
				others; but I created them using scatter (XY) charts and factions 
				from <strong>Kyd Series</strong>. Because these charts don't use 
				Excel's stock chart types, worksheet factions can 
				control the color and thickness of each line or bar for each day 
				shown. 
				</p>
				<p>The key factions used for the second and fourth charts are:</p>
				<ul>
					<li><strong>KydRGB3_RGB</strong>, which converts red, 
					green, and blue values in the range 0-255 to the single RGB 
					value required by KydSetSeriesLineColor.<br />
					</li>
					<li><strong>KydSetSeriesLineColor</strong>, which gives 
					worksheet formulas the power to set the color of any line in 
					any chart.<br />
					</li>
					<li><strong>KydSetSeriesLineWidth</strong>, which gives 
					worksheet formulas the power to set the width of any line in 
					any chart.</li>
				</ul>
				<p>As you'll see in the following examples, I use both <strong>
				KydSetSeriesLineColor</strong> and <strong>KydSetSeriesLineWidth</strong> 
				frequently with these cutting-edge charts. <br />&nbsp;
				</p>
				<h3>Example 3: Use Lines and Columns as Traffic Lights </h3>
				<p>
				Charts make it very easy for managers to review a massive number 
				of performance indicators quickly. Even so, managers tend to experience at least two challenges 
				when you give them charts. </p>
				<p>
				First, as they glance over reports, they must pause at 
				each chart to determine whether an upward trend is a good result 
				(with sales, for example) or a bad result (with costs, for example). They 
				could scan results much more quickly if each chart made it 
				obvious whether the current results show good or bad news. </p>
				<p>
				Second, managers often want to concentrate on the really 
				bad news first. Then, if they have time, they can pay attention 
				to other 
				measures of performance. But which charts show really bad news?</p>
				<p>
				These traffic-light charts simplify both challenges:</p>
				<ul>
					<li>The current period shows good news in green, bad news 
				in red, and indifferent news with no change in color. That 
					leaves no question about whether the new trends are good or 
					bad.<br />
&nbsp;</li>
					<li>When the current period shows <i>really</i> bad news, 
				the chart automatically displays it using a larger segment or 
					column. This causes the 
					really bad result to &quot;pop out&quot; on a page. This pop-out effect allows your 
				managers to instantly see every chart on a page that shows 
				really bad news. Your spreadsheet formulas determine what 
				&quot;bad news&quot; means.</li>
				</ul>
				<p>(For color-blind managers, you can use different colors—orange and blue, for example, rather than 
				red and green.)&nbsp; </p>
				<p>These traffic-light charts use several factions and functions 
				from <strong>Kyd Series</strong>:</p>
				<ul>
					<li><strong>KydGetSeriesPointsCount</strong> counts 
					the number of points in the chart, which automatically tells 
					the factions which segment (the last segment) should be 
					modified</li>
					<li><strong>KydSetSeriesFillColor </strong>controls the fill 
					colors of the column chart.</li>
					<li><strong>KydSetSeriesLineColor </strong>controls the line 
					colors.</li>
					<li><strong>KydSetSeriesLineWidth</strong> controls the widths 
					of both the line plot and the border of the column plot.</li>
					<li><strong>KydRGB3_RGB</strong> makes it easy to 
					specify the required RGB color.</li>
				</ul>
				<br/>
				<h3>Example 4: Use Bullet Plots to Replace Gauges</h3>
				<p>Bullet charts show what most gauges do, but more clearly and in a smaller space.</p>
				<p>The orange bars show actual performance; the black lines 
				show targeted performance; and the sets of three blue bars show bad 
				(dark blue), 
				average, and good performance.</p>
				<p>This chart relies on these functions and factions from
				<strong>Kyd Series</strong> 
				and <strong>Kyd Axes:</strong> </p>
				<ul>
					<li><strong>KydRGB3_RGB</strong> makes it easy to 
					specify the required RGB color.</li>
					<li><strong>KydSetDataLabelText</strong> manages the X-axis 
					labels, which, in this example, actually are data labels associated with a 
					hidden line.</li>
					<li><strong>KydSetSeriesLineColor</strong> controls the line 
					colors.</li>
					<li><strong>KydSetSeriesLineWidth</strong>&nbsp;controls the 
					width of the blue and orange columns and the thickness of 
					the black horizontal line. </li>
				</ul>
				<br/><h3>Example 5: Diagnostic Chart for Sales </h3>
				<p>Diagnostic charts aren't limited to stock analysis. Here, for example, worksheet factions from <strong>Kyd Series</strong> 
				mark the 
				holidays in a chart of daily retail sales. Two of the marks 
				correspond with noticeable peaks in sales. But the peak in late July is 
				unexplained. If research shows that a special promotion was held 
				at that time, Kyd Series could mark that day with a dot of a 
				different color, to indicate a special promotion.</p>
				<p>Notice that one holiday showed no increase in sales. This could 
				signal a missed opportunity to increase sales in the following 
				year.</p>
				<p>Notice that the X axis uses a number format that causes 
				the day, month, and year to appear in three separate rows. A 
				faction from <strong>Kyd Axes</strong> performs this magic. I've found no 
				way to assign this number format using standard Excel.</p>
				<p>
				This chart relies on these functions and factions...</p>
				<ul>
					<li> <strong>KydRGB3_RGB</strong> to specify the RGB value 
					for the marker color.</li>
					<li><strong>KydSetMarkerFillColor</strong>, which allows 
					spreadsheet formulas to set the fill color of the round chart 
					markers.</li>
					<li> <strong>
				KydSetMarkerSize,</strong> which sets the size of the red 
					markers.</li>
					<li><strong>KydSetAxisLabelNumberFormat</strong>, which 
					gives your formujlas the ability to set the number format of 
					labels for the chart's axes.</li>
				</ul>
				<br/><h3>Example 6: New Chart Type...H Plot</h3>
				<p>One of the great benefits of using the <b>Kyd Series </b>and
				<b>Kyd Axes </b>add-ins is that it gives you many more options 
				for displaying data clearly. In fact, if you need to create a 
				new type of chart, you probably can do so.</p>
				<p>For example, a recent news report said that people in the US 
				are finding jobs more quickly. So I got curious and 
				downloaded data for the Mean Duration of Unemployment from the 
				web site of the Federal Reserve Board of St. Louis.</p>
				<p>I could have charted the entire data series from 1967, but 
				that would have compressed recent trends too much. I could have 
				started the chart in recent years, but that would have hidden 
				relevant historical data. So I invented this &quot;H&quot; chart. </p>
				<p>The left orange column shows the maximum and minimum performance over 
				the previous 30 years. The black bar shows average monthly 
				unemployment duration during that time. The right bar and column show similar results for the past 
				two years. And the blue line shows the two-year trend.</p>
				<p>Notice how the first and last labels in the horizontal axis 
				are completely different from the other labels. Factions from the <strong>Kyd Labels Lite</strong> 
				add-in manage these labels. In fact, creating this chart would 
				have been difficult if this add-in hadn't given me the power to 
				label the orange bars properly.</p>
				<p>This chart relies on two spreadsheet factions: </p>
				<ul>
					<li><strong>KydSetSeriesLineColor,</strong> which sets the 
					with the orange, blue, and black lines.<br />
					</li>
					<li><strong>KydSetDataLabelText,</strong> which writes text 
					in cells as text in data labels attached to a hidden chart 
					series set up to display those labels.</li>
				</ul>
				<br/><h3>Example 7: Irregular Dates</h3>
				<p>This chart shows actual results from five surveys for a US 
				Senate race in Washington State. Results for the Republican 
				candidate are in red and for the Democrat candidate are in blue. </p>
				<p>Do you see anything unusual about this chart? I see three things.</p>
				<p>1. The dates aren't periodic. That is, they're not evenly 
				spaced. How is it possible to label non-periodic data in 
				Excel charts?</p>
				<p>2. Partial gridlines connect the largest 
				data point 
				to each label. (I also could have used full gridlines, however.) How is 
				it possible to display&nbsp;non-periodic, partial-height 
				gridlines like these?</p>
				<p>3. As in a previous chart, the day, month, and year labels are in 
				three rows. How is that possible to do in Excel?</p>
				<p>You can use worksheet factions found in <strong>Kyd Series</strong> and <strong>Kyd Axes</strong> 
				to accomplish all three effects:</p>
				<ul>
					<li> <strong>
				KydSetSeriesLineColor</strong> sets the colors of both lines. 
					(Controlling the line colors with a faction wasn't really 
					necessary here, but it allowed me to change the colors 
					easily.)<br />
					</li>
					<li><strong>KydRGB3_RGB </strong>calculates the RGB value 
					for the lines. <br />
					</li>
					<li><strong>KydSetAxisScaleMinimum</strong> and <strong>
					KydSetAxisScaleMaximum</strong>, set the minimum and 
					maximum values of the specified value axis, here, the Y 
					axis. <br />
					<br />
					(Note: If I hadn't used these factions, Excel would have set 
					a maximum value that was too large, and a minimum value of 
					zero. This would have made it difficult to see the gaps 
					between the two lines. Of course, I could have manually set 
					the max and min values, but I probably would have needed to 
					adjust those settings each time I added new data. On the 
					other hand, these two functions let my spreadsheet set the 
					Y-axis values automatically.) <br />
					</li>
					<li> <strong>KydSetDataLabelText</strong> assigns the date 
					serial numbers for the dates shown.<br />
					</li>
					<li><strong>KydSetDataLabelNumberFormat</strong> assigns the 
					number format for the dates.</li>
				</ul>
				<br/><h3>Example 8: Dual X Axes</h3>
				<p>A chart like this in the Wall Street Journal compared stock 
				performance during the 1930s with that of today. </p>
				<p>The WSJ chart used shades of gray. But here, the blue 
				dates label the blue line, 
				and the red dates label the red line. </p>
				<p>The two sets of X-axis labels actually are data labels 
				associated with two invisible lines plotted at the bottom of the 
				chart. Then two KydAxis functions manage those labels:</p>
				<ul>
					<li><strong>KydSetDataLabelText</strong> writes either the 
					date or a null string to the label, as determined by a 
					formula.<br />
					</li>
					<li><strong>KydSetDataLabelNumberFormat</strong> formats the 
					dates as shown, and ignores the null strings.<strong>
					</strong></li>
				</ul>
				<p>This type of chart could be very useful for comparing 
				performance after two different events. For example, you could 
				compare the change in sales during a prior recession to the 
				current one, or changes in certain expenses for prior and 
				current 
				managers, 
				or web statistics after a major change in your web 
				site, or whatever.</p>
				<br/><h3>Example 9: Auto-Format the Y Axis</h3>
				<p>If you want your charts to look professional, this example illustrates a feature that will save a lot of time. The feature solves a problem that occurs when 
				you update existing reports and analyses that 
				use charts.</p>
				<p>The value axis (the Y axis) in the top chart uses the General 
				number format. That format is ugly and unprofessional, so you 
				apply the &quot;#,##0&quot; number format. But when your data changes, 
				you occasionally get a result like the middle chart. So you must 
				manually change the format to &quot;#,##0.0&quot;, as in as in the 
				third chart. </p>
				<p>But since maintaining the Y axis manually takes time and 
				effort, most users just add an extra zero or two to the Y axis. 
				This works, but clutters the axis with unneeded zeros.</p>
				<p>The real problem is that standard Excel doesn't offer 
				automatic number formatting. But that's what a <strong>Kyd Axes</strong> 
				worksheet faction offers, as shown in the third chart. </p>
				<p>The formula for the third chart is:</p>
				<p><strong>=KydSetAxisTickLabelAutoFormat(B30,2,,&quot;#,##0&quot;)</strong></p>
				<p>Here, cell B30 is any cell covered by the chart. The number 2 
				refers to the Y axis. And &quot;#,##0&quot; is the seed format. The 
				faction adds as many decimal positions to this format as needed 
				to display the specified axis correctly. Here, for example, it 
				needed to add only one decimal position. </p>
				<p>If you often update existing charts with new data, and if you care about 
				the appearance of your charts, you'll use this faction a lot.</p>
