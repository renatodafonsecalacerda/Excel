				<h2 class="rubric">
				<!-- #BeginEditable "rubric" -->
				That's right. Your worksheet formulas 
				really can change chart settings. They can assign colors, 
				change line widths, resize markers, format labels, and more. One Excel MVP calls it magic!<!-- #EndEditable --></h2>
				<br />
				<table cellpadding="0" cellspacing="0" style="width: 100%">
