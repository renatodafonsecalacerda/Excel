function showTMAds(NbrAds){
var Arr=getAdsArr()
var IdxArr=new Array();
for (var i=0; i<Arr.length; i++){
	IdxArr[i]=i
	}
for (var i=0; i<NbrAds; i++){
	var RandIdx=Math.floor(Math.random()*(Arr.length-i))
	var temp=IdxArr[RandIdx]
	IdxArr[RandIdx]=IdxArr[IdxArr.length-1-i]
	IdxArr[IdxArr.length-1-i]=temp
	}
var sMsg = ""
for (var i=Arr.length-1;i>Arr.length-1-NbrAds;i--){
	sMsg += "<div style='float:left; background-color:#DDFFDD;"
	sMsg += "font-family:Arial, Helvetica, sans-serif; text-align:left;border-top:solid #000088 1px; font-size: small; padding: 5px;'>"
	sMsg += Arr[IdxArr[i]]
	sMsg += "</div>"
	}
//alert(sMsg)
//document.write(sMsg)
return (sMsg)
	}
