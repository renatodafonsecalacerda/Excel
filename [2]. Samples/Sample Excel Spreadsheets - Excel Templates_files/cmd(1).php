				<img class="image-right" alt="Mini-chart for Excel dashboards" height="155" src="images/mini-charts.gif" width="406" />2. How to Create Mini-Charts for Dashboard Reporting.</strong> 
				The chart on the left is a typical Excel chart that's been 
				reduced to a small size. The chart on the right is a <em>
				mini-chart</em>, which is a key ingredient of Excel dashboard reporting. </p>
				<p>The step-by-step instructions in this chapter show you how to 
				easily convert your charts from small, unreadable ones into 
				mini-charts that your managers can read easily.</p>
		
		<p><strong>3. Charting 
		Techniques for Dashboard Reporting.</strong> There's more to dashboard reporting 
		than merely creating small charts. This chapter discusses a variety of 
		other techniques to enhance charting for dashboard reporting. I compare 
		gauges, which Excel doesnt support, to far better display methods that 
		Excel does support.</p><p><strong>4. Create Figures that Use Both Charts and 
		Worksheets.</strong> Much of dashboard reporting uses spreadsheet cells to 
		complement charts. This chapter describes typical techniques. The 
		chapter also explains how to force charts to work together. In<span class="style14"> the 
		dark-red 
		figure on the right below,</span> for example, each column of charts uses the same Y-axis 
		values. This allows readers to compare each companys 
		performance within each column of the report.</p><p><strong>
				<img class="image-right" alt="Excel camera tool" height="234" src="images/camera2.gif" width="130" />5. How to Use Excels Camera 
		Tool.</strong> Most users ignore Excel's Camera Tool, but it's crucial for 
		adding tables to Excel 
		dashboard reports. This chapter describes a variety of 
		reporting techniques that only Excel's Camera Tool can achieve. </p>
				<p>For example, 
				in dashboards we often need to display tables that use row or 
				column spacing that doesn't line up with with the worksheet that 
				displays the tables. And we occasionally need to rotate a table 
				for various reasons. </p>
				<p>In this extreme example, the boundaries of the two tables 
				don't line up with the underlying worksheet; and both tables 
				have been rotated 90 degrees. However, both tables still are dynamic. 
				That is, their values still change as the data changes.</p>
				<p>This chapter explains many reasons to use the Camera Tool, 
				and how to use it easily. </p>
				<p><strong>6. How 
		to Funnel Data into Dashboard Reports.</strong> At first glance, dashboard 
		reporting is all about ways to create a great-looking report page. But 
		the real challenge for Excel users is this: How do we update Excel 
		easily so that dashboards dont quickly send us to Spreadsheet Hell? 
		This chapter explains the key ways to flow data into your dashboards 
				easily, including 
		PivotTables, Excel-friendly OLAP, and the INDEX-MATCH functionality.<br />
		<br /><strong>7. How to Use Spreadsheet Databases for Dashboard Reporting.</strong> In my 
		experience, most spreadsheet databases are badly managed. As a 
		consequence, they often cause more trouble than they're worth. This 
		chapter explains a simple way to set up spreadsheet databases for 
		reliable spreadsheet reporting.</p><p>
			<strong>8. How to Build Dashboard Reports 
		in Excel.</strong> This is the key chapter in the book. It explains the steps 
		necessary to create this Excel dashboard report. Here, the charts are 
				all mini-charts (chapter 2) and the tables rely on the Camera 
