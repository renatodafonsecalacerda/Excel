			Here's a 
		description of each chapter:<p>
			<strong>1. The Advantages of Dashboard 
		Reporting With Excel.</strong> This short introductory chapter tells much about 
		what you already know if you spend much time on this site: Excel can 
		produce great dashboard reports. </p>
			<p>I call them &quot;magazine-quality&quot; reports. 
