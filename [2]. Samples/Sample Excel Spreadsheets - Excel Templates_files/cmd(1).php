		trends and exceptions that I needed to recognize as serious problems and 
		opportunities. I hated the thought that I was missing so much treasure 
		hidden in so much clutter. <p>As a spreadsheet user, I hated to 
		prepare those reports. I hated to spend long hours, month after month, 
		turning the crank, producing reports that people often ignored.<br />
		<br />(One time, I accidentally printed three copies of the same page in the same report. No one noticed.)</p>
				<p>Then I found a solution an old copy of the <em>Harvard Business Review</em>.</p><p>The article 
				was 
		written in 1979 by George Blake, the VP Finance of one of the 
		largest companies in Mexico. It explained how his company was using many 
		small graphs on one page to report management performance.</p>
