				<h3>The Excel Dashboard E-Book Kit</h3>
				<p>My ebook, <i>Dashboard Reporting With Excel</i>, teaches you 
				how to create Excel dashboards from scratch. It doesn't discuss 
				the reports on this page specifically, but it does explain how 
				to set up the charts, tables, and formatting that I used to 
				create these reports.</p>
				<p>The kit consists of the 200-page e-book and a package of more 
				than 20 files that the book discusses. Here are its major 
				topics:</p>
				<p><b>How to Create Mini-Charts.</b> Excel users often print 
				charts about the size of post cards. But business magazines 
				often print charts about the size of postage stamps. By using 
				small charts in our reports, we give our audience more insight 
				more quickly than any other method of reporting. Chapter 2 of 
				the e-book explain how to create those magazine-quality 
				&quot;mini-charts.&quot; And the workbooks that accompany the e-book 
				illustrate each step along the way.</p>
				<p><b>Other Useful Techniques for Dashboards.</b> Good 
				dashboards often require additional charting techniques. In 
				Chapter 3, I explain how to add an additional data series to a 
				chart, how to include multiple chart types, how to overlay two 
				charts to create special effects, how to use auto-shapes in 
				charts, and so on. This chapter also explains why you shouldn't 
				use gauges in dashboard reports.</p>
				<p><b>How to Create Figures that Use Both Charts and Worksheets.</b> 
				In dashboards, cell formatting, column widths, row heights, 
				special formulas, and other spreadsheet elements all combine 
				with charts to create the figures we see. Chapter 4 explains how 
				to use these elements in your dashboard reports.</p>
				<p>
				<b>Excel's Camera Tool.</b> This long-time Excel feature 
				probably is the most-useful feature that most Excel users have 
				never heard of. Also known as a <em>picture link,</em> it gives 
				you the ability to position live images of tables anywhere in 
				your dashboard report.&nbsp; </p>
				<p>And because you even can set up a formula that determines 
