				<p>Do most of your Excel reports look somewhat like the gray 
				examples at the right above? </p>
				<p>If so, your audience must work <i>hard</i> to convert that 
				mass of data into useful insight. But your Excel reports don't 
				have to be that way. They could be <i>easy</i> to read and 
				understand. </p>
				<p>This page shows many dashboard reports and figures created 
				entirely with Excel. These &quot;magazine-quality&quot; dashboards act 
				like the dashboard of your car. They can present a massive 
				amount of useful information quickly and easily. </p>
				<p>Most Business Intelligence (BI) software costs many thousands 
				of dollars. It typically displays much less information per page 
				or screen than these reports do. And Subject Matter Experts like 
				you typically have less control over those expensive displays 
				than the IT department does. </p>
				<p>But because you already own Excel, you can create reports 
				like this for free. You only need ordinary Excel workbooks, with 
				no macros. </p>
				<p>To be clear, because you already own Excel you pay <i>nothing</i> 
				for the software needed to create these dashboard reports.</p>
				<p>Excel users in more than 160 countries are using Excel to 
				create reports like these for their managers, clients, 
				co-workers, or investors. You can see testimonials from some of 
				them on this page. </p>
				<p>You can create your own professional-quality reports, using 
				Excel on either a Windows or Mac computer.</p>
				<p>Of course, if there's something you want to change about 
				these reports, you can do so easily. This isn't complex 
				software. It's only Excel. <br />&nbsp;
				</p>
				<h3>Three Dashboard Kits to Choose From</h3>
				<p>I offer three ways to create dashboard reports. </p>
				<p>1. If you need a dashboard report in a hurry, you can use my 
				new Quick-and-Easy Dashboard Kit Version 2 (QnE2). To generate 
				your first report in a few hours you merely...</p>
				
				<ol style="list-style-type:lower-alpha">
					<li>Choose a portrait or landscape report format. </li>
					<li>Enter your actual and target data (when you have 
					targets) in data worksheets.</li>
					<li>Apply a color theme.</li>
					<li>Recalculate your workbook (press F9) if your manual 
					calculation mode is set in Excel.</li>
					<li>Print your magazine-quality report, or save it as a PDF 
					file.</li>
				</ol>
				<p>2. If you need tables in your report, or if you want to 
				emphasize certain measures over others, you can use Version 2 of 
				the Database Dashboard Kit (DB2). It takes a little longer to 
				create your tables, but otherwise, the formulas, format, and 
				data worksheets are the same as for QnE2. </p>
				<p>3. If you want to learn how to create your own Excel 
				dashboard reports from scratch. My <em>Excel Dashboard E-Book 
				Kit </em>provides a 200-page e-book and sample files that teach 
				you what you need.<br />&nbsp;
				</p>
				<h3>Overview of the Dashboard Kits</h3>
				<p>The reports in IncSight QnE2 and IncSight DB2 look like they 
				came from the pages of a business magazine. Unless you tell your 
				audience, they probably won't guess that you created your 
				reports in Excel.</p>
				<p>The dashboard kits are designed for Excel 2007, Mac Excel 
				2008, and above. (If you use earlier versions of Excel, see
				<a href="../templates/is-qne.htm">
				IncSight QnE</a>,
				<a href="../templates/is-db.htm">
				IncSight DB</a>, and
				<a href="../ebooks/ebook02.htm">
				Dashboard Reporting With Excel</a>.)</p>
				<p>I created these reports with one goal in mind: As Excel 
				users, our purpose is to communicate business information 
				clearly. That is, our purpose is <i>not</i> to impress our 
				audience with pretty pictures. </p>
				<p>
				Therefore, these reports avoid <i>chart junk</i>. This is Edward 
				Tufte's term for the distracting eye candy that many commercial 
				dashboard companies add to their expensive dashboard reports.
				</p>
				<p>Although the two IncSight dashboard kits use the same 30-page 
				instruction manual, you probably will need only its first two 
				pages, which offer Quick-Start instructions. Other pages explain 
				how to modify your dashboards, and explain some of their finer 
				points. For example, if you want to change from monthly reports 
				to daily reports, or if you want to report dozens of periods of 
				performance, the documentation explains how to do it.</p>
				<p>After your data is ready you should be able to create your 
				first dashboard report in an hour or two. And you should be able 
				to update your reports each month in less than five minutes. </p>
				<p>(Why does updating go so quickly? It's because you keep your 
				data in a separate data worksheet, which all your dashboard 
				reports can use. So you just update your data once; then in each 
				report workbook, you change the date in a cell to tell your 
				report to display your new data.) </p>
				<p>Later, if you care to do so, you can reduce your update time 
				to nearly nothing by using the techniques I describe in the <i>
				Excel Dashboard E-Book Kit.</i> For example, you could use 
				Excel's GETPIVOTDATA function to link your data worksheets to 
				PivotTables.</p>
				<p>To get started with either set of reports, you need to take 
				only three general steps:</p>
				<p><b>1. Choose a dashboard template workbook.</b> Each kit of 
				dashboard templates offers different report formats,<span class="style18"> 
				which I describe below</span>. And you'll find examples from 
				both <span class="style18">kits below</span>.</p>
				<p>
				<b>2. Enter your titles and data codes. </b>Each chart in a 
				report workbook is linked to a support worksheet, which uses 
				formulas to return data from your data worksheets. These 
				formulas rely on the report date and on the data codes you 
				assign to each row of data. The data codes could be product 
				codes, general ledger account numbers, SKUs, Series IDs, and so 
				on. To illustrate, if you want chart 5 to report data for code 
				C023, you enter &quot;C023&quot; in one cell in the support worksheet for 
				chart 5, and the formulas do the rest. </p>
				<p>If you own the <i>Excel Dashboard E-Book Kit </i>(below), you 
				can enter the formulas I discuss in chapters 6 - 8, formulas 
				that link your dashboard workbook to your Excel data worksheets, 
				or to corporate databases.</p>
				<p><b>3. Choose your color themes.</b> Each IncSight kit 
				includes two color themes. And additional color themes are 
				available. Use a simple Excel command to apply any color scheme 
				to your report workbook. Experiment until you find the one you 
				like best. If you don't like a particular color in one of the 
				color schemes, just change it. </p>
				<p>Then print your first report, or save it as a PDF file. </p>
				<p>Monthly, you can follow a simple process to update your 
				report quickly. Maintaining your report workbook is easy. There 
				are no macros to break. Nothing is hidden or protected. It's 
				just ordinary Excel.<br />&nbsp;
				</p>
				<h3>Flexible Reporting</h3>
				<p>When you use your Excel dashboards for management reporting, 
				you'll have better results if you remember how flexible these 
				dashboards are. This flexibility can improve the value of your 
				reports significantly...</p>
				<ul>
					<li>Remember that when you create your first dashboard 
					report, the first measures you include don't need to be 
					perfectly chosen. This is because your audience quickly will 
					suggest other measures they want to see. <br />
					<br />
					For most &quot;real&quot; reporting solutions, changes like this can 
					be an expensive problem, but not for your dashboard 
					templates. If the data is available, you can replace one 
					measure with another in about ten seconds. So the important 
					thing is to get your first dashboard report into your 
					managers' hands as quickly as possible.<br />
					<br />
					</li>
					<li>Remember that different managers have different 
					information needs...and that's okay. <br />
					<br />
					With normal Excel reports, that can be a huge problem 
					because normal Excel reports typically take a lot of time to 
					generate. But it's not a problem with these dashboards. This 
					is because you'll be able to open, update, and print each 
					manager's report in only a few seconds. So you could update 
					and generate dozens of different reports in less than half 
					an hour.<br />
					<br />
					</li>
					<li>Remember that you can report data from <em>any</em> 
					source; you're not trapped within the limits of your 
					corporate database.<br />
					<br />
					So if your managers want your reports to include data from 
					&quot;silo&quot; systems, or from your web site, or about your 
					competitors, or about the economy, or from a log that some 
					intern maintains...that's not a problem. You just add the 
					data to your spreadsheet database and treat it like any 
					other data in your dashboard reports.</li>
				</ul>
				<p>In &nbsp;short, I've designed these dashboard templates to be used 
				as a perpetual prototype that you can adapt quickly to changing 
				conditions, to changing opinions, to changing managers, and to 
				changes in your managers' information needs. So be sure to take 
				full advantage of this power.<br />&nbsp;
				</p>
